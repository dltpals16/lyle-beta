def get_health_check(phase: str) -> dict | None:
    """
    현재 시술 단계(current_phase)에 해당하는 건강 체크 데이터를 반환합니다.
    정확한 키 매칭 → 부분 매칭 순서로 탐색합니다.
    """
    if not phase:
        return None

    # 정확한 매칭
    if phase in TIMELINE_HEALTH_CHECK:
        return TIMELINE_HEALTH_CHECK[phase]

    # 부분 매칭 (예: "과배란 유도" → "과배란 유도 시작")
    phase_lower = phase.strip().lower()
    for key, data in TIMELINE_HEALTH_CHECK.items():
        if phase_lower in key.lower() or key.lower() in phase_lower:
            return data

    return None


# 배아 종류별 판정일 단계 경계
_JUDGMENT_BOUNDARIES = {
    EMBRYO_BLASTOCYST: {  # 5일 배아(배반포)
        "early_end": 4,          # D+1 ~ D+4
        "middle_end": 7,         # D+5 ~ D+7
        "before_judgment_end": 11,  # D+8 ~ D+11 (판정일 D+9~11)
    },
    EMBRYO_DAY3: {  # 3일 배아
        "early_end": 5,          # D+1 ~ D+5
        "middle_end": 9,         # D+6 ~ D+9
        "before_judgment_end": 14,  # D+10 ~ D+14 (판정일 D+11~14)
    },
}


def get_judgment_message(
    days_after_transfer: int,
    name: str = "",
    embryo_type: str = EMBRYO_BLASTOCYST,
    result: str | None = None,
) -> str:
    """
    이식 후 경과 일수에 따른 판정일 카운트다운 메시지를 반환합니다.

    Args:
        days_after_transfer: 이식 후 경과 일수
        name: 사용자 이름
        embryo_type: 배아 종류 (EMBRYO_DAY3 또는 EMBRYO_BLASTOCYST)
        result: 판정 결과 ("positive", "negative", None)

    Returns:
        포맷된 메시지 문자열
    """
    # 판정 결과가 나온 경우
    if result == "positive":
        return JUDGMENT_COUNTDOWN["after_judgment_positive"].format(
            days=days_after_transfer, name=name,
        )
    if result == "negative":
        return JUDGMENT_COUNTDOWN["after_judgment_negative"].format(
            days=days_after_transfer, name=name,
        )

    # 배아 종류에 따른 단계 경계 결정
    bounds = _JUDGMENT_BOUNDARIES.get(embryo_type, _JUDGMENT_BOUNDARIES[EMBRYO_BLASTOCYST])

    if days_after_transfer <= bounds["early_end"]:
        template = JUDGMENT_COUNTDOWN["early"]
    elif days_after_transfer <= bounds["middle_end"]:
        template = JUDGMENT_COUNTDOWN["middle"]
    elif days_after_transfer <= bounds["before_judgment_end"]:
        template = JUDGMENT_COUNTDOWN["before_judgment"]
    else:
        template = JUDGMENT_COUNTDOWN["default"]

    return template.format(days=days_after_transfer, name=name)
